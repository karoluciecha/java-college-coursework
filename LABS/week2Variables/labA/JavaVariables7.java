public class JavaVariables7 {
    public static void main(String[] args) {
        float number1 = 12.3f, number2 = 5.5f;
        System.out.println("Number 1 + Number 2 = " + (number1 + number2));
        System.out.println("Number 1 - Number 2 = " + (number1 - number2));
        System.out.println("Number 1 * Number 2 = " + (number1 * number2));
        System.out.println("Number 1 / Number 2 = " + (number1 / number2));
        System.out.println("Number 1 % Number 2 = " + (number1 % number2));
        System.out.println("Number 2 - Number 1 = " + (number2 - number1));
        System.out.println("Number 2 % Number 1 = " + (number2 % number1));
    }
}
