public class JavaIf3 {
    public static void main(String[] args) {
        double number1 = 12.25;
        if (number1 < 15) {
            System.out.println("Number is less than 15");
        }
        else if (number1 == 15) {
            System.out.println("Number is equal to 15");
        }
        System.out.println("END OF PROGRAM!");
    }
}