public class JavaIf4 {
    public static void main(String[] args) {
        int n1 = 0;
        if (n1 < 0) {
            System.out.println("Value of n1 is " + n1 + ". That's negative!");
        }
        else if (n1 >= 0) {
            System.out.println("Value of n1 is " + n1 + ". That's positive!");
        }
    }
}